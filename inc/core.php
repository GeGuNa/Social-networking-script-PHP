function getAmount($amount, $length = 0)
{
    if(0 < $length){
        $amount = round($amount, $length);
    }else{
        $amount = round($amount,2);
    }
    return $amount + 0;
}

function removeElement($array, $value)
{
    return array_diff($array, (is_array($value) ? $value : array($value)));
}



