function numberSlotResult($win,$num){
    for ($i=0; $i <$win ; $i++) {
        $res[] = $num;
    }
    $left = 3-$win;
    while ($left >0) {
        $newRand = mt_rand(0, 9);
        if($newRand != $num){
            $res[] = $newRand;
            $left--;
        }
    }
    shuffle($res);
    return $res;
}
